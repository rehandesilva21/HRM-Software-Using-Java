        String search = jTextField8.getText();

        try{

            DefaultTableModel dt= (DefaultTableModel) jTable1.getModel();

            dt.setRowCount(0);

            Statement s =DBconnect.getConnection().createStatement();

            ResultSet rs =s.executeQuery("SELECT * FROM `Employees` WHERE Name = '"+search+"' OR EPF_no = '"+search+"'OR Department_Code = '"+search+"' OR Designation = '"+search+"'" );

            while (rs.next()){

                Vector v = new Vector ();
                v.add(rs.getString(1));
                v.add(rs.getString(2));
                v.add(rs.getString(3));
                v.add(rs.getString(4));
                v.add(rs.getString(5));

                dt.addRow(v);

            }

        }
        catch(Exception e){

        }
