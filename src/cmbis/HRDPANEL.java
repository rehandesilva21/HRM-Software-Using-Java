    private void jTextField9KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField9KeyReleased
        //To search table by Branch,Department or code
        String search = jTextField9.getText();

        try{

            DefaultTableModel dt= (DefaultTableModel) jTable2.getModel();

            dt.setRowCount(0);

            Statement s =DBconnect.getConnection().createStatement();

            ResultSet rs =s.executeQuery("SELECT * FROM Departments WHERE Branch = '"+search+"' OR Department = '"+search+"'OR Code = '"+search+"' " );

            while (rs.next()){

                Vector v = new Vector ();
                v.add(rs.getString(1));
                v.add(rs.getString(2));
                v.add(rs.getString(3));

                dt.addRow(v);

            }

        }
        catch(Exception e){

        }
    }//GEN-LAST:event_jTextField9KeyReleased

