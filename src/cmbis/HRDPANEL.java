    private void button5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button5ActionPerformed
        PreparedStatement add;
        //to make a connection to the database and insert data to departments table
        try{
            Connection con = DBconnect.getConnection();
            add = con.prepareStatement("INSERT INTO `Departments` (`Branch`, `Department`, `Code`) VALUES (?,?,?)");
            add.setString(1, jTextField1.getText());
            add.setString(2, jTextField2.getText());
            add.setString(3, jTextField3.getText());
            
            String branch = (jTextField1.getText());
            String dep = (jTextField2.getText());
            String code = (jTextField3.getText());
            
            //to Write data in a text file
            try{
                FileWriter Writer = new FileWriter("Departments.txt",true);
                Writer.write("Branch: "+branch+"  Department: "+dep+"  department Code: "+code+"");
                Writer.write(System.getProperty("line.separator"));
                Writer.close();
                JOptionPane.showMessageDialog(null,"Successfully Insert to the text file");
                
}
            catch (Exception e){
            }

            if (add.executeUpdate()!=0 ){
                JOptionPane.showMessageDialog(null,"A new department added successfully!!");
                tbload();

            }
            else{
                JOptionPane.showMessageDialog(null,"Something went wrong");

            }
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");

        }
        catch(SQLException | ClassNotFoundException e){

            Logger. getLogger(HRDPANEL.class.getName ()) .log (Level.SEVERE, null, e) ;

        }
    }//GEN-LAST:event_button5ActionPerformed

